from datetime import datetime


def get_system_prompt() -> str:
    """Возвращает системный промпт с актуальной датой."""
    today = datetime.now().strftime("%Y-%m-%d")
    return SYSTEM_PROMPT_TEMPLATE.replace("{TODAY}", today)


